#include "Char.hpp"

/*
** ------------------------------- CONSTRUCTOR --------------------------------
*/

Char::Char(std::string input) : _input(input)
{
}

Char::Char( const Char & src )
{
	*this = src;
}


/*
** -------------------------------- DESTRUCTOR --------------------------------
*/

Char::~Char()
{
}


/*
** --------------------------------- OVERLOAD ---------------------------------
*/

Char &				Char::operator=( Char const & rhs )
{

	if ( this != &rhs )
	{
		this->_input = rhs._input;
	}
	return *this;
}

